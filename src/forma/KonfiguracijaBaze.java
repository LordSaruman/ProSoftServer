    private void btnSaveConfigurationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveConfigurationActionPerformed
            String url = txtURL.getText().trim();
            String username = txtUsername.getText().trim();
            String password = txtPassword.getText().trim();
            
            if(izvrsiValidaciju(url, username, password) == true){
            
            DatabaseParameters databaseParameters = new DatabaseParameters(url, username, password);
            try {
                Kontrolor.getInstance().sacuvajParametre(databaseParameters);
                JOptionPane.showMessageDialog(this, "Parameters have been successfully saved!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                Logger.getLogger(KonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, e);
            }
            }else{
                JOptionPane.showMessageDialog(null, "Failed validation. Check all fields.", "Error", JOptionPane.ERROR_MESSAGE);
                invalidate();
                repaint();
                validate();
                return;
            }
           
        
    }//GEN-LAST:event_btnSaveConfigurationActionPerformed

