    private void popuniTabeluKonfiguracija() {
        
        try {
            DatabaseParameters databaseParameters = Kontrolor.getInstance().vratiParametre();
            txtURL.setText(databaseParameters.getURL());
            txtUsername.setText(databaseParameters.getUsername());
            txtPassword.setText(databaseParameters.getPassword());
            
        } catch (Exception e) {
            Logger.getLogger(KonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    private boolean izvrsiValidaciju(String url, String username, String password) {
        boolean validnaForma = true;
        if (url == null || url.isEmpty()) {
            jlblurl.setText("Field for url is empty.");
            jlblurl.setForeground(Color.red);
            validnaForma = false;
        } else {
            jlblurl.setText("");
        }
        
        if (username == null || username.isEmpty()) {
            jlblusername.setText("Field for username is empty.");
            jlblusername.setForeground(Color.red);
            validnaForma = false;
        } else {
            jlblusername.setText("");
        }
        
        if (password == null || password.isEmpty()) {
            jlblpassword.setText("Field for password is empty.");
            jlblpassword.setForeground(Color.red);
            validnaForma = false;
        } else {
            jlblpassword.setText("");
        }
        
        if (!validnaForma) {
            JOptionPane.showMessageDialog(this, "You didn't provide info for all fields on the form or you have some input errors.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
